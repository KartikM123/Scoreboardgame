    public void setStrategySection() throws FileNotFoundException{
                lQuarterCount.setText("Quarter " + (qnum+1));

        int size = 0;
        for (Player player: sc.home.players){
        size++;
        }
        int [] scores = new int[size];
        size =0;
        for(Player player: sc.home.players){
            scores[size] = player.scored;
            size++;
        }
        CalculatedStats offense = new CalculatedStats(sc,true,scores);
    size = 0;
    for (Player player: sc.away.players){
        size++;
        }
    int [] scores1 = new int[size];
        size =0;
        for(Player player: sc.away.players){
            scores[size] = player.scored;
            size++;
        }
        CalculatedStats defense = new CalculatedStats(sc,false,scores);
        StrategyDecision strat = new StrategyDecision (offense,defense);
        lStrategyName.setText("Offense Strategy Name: " + strat.offensestrategy.stratname );
                lStrategyDescription.setText("Offense Strategy Description: " + strat.offensestrategy.description );
                    lReminders.setText("Reminders: \n" + strat.extremes);
        lOffrating.setText("Offense Rating: " + strat.offensestratrating);
        ldefrating.setText("Defense rating: "+ strat.defensestratrating);
        lDefenseStrategyName.setText("Defense Strategy Name: " + strat.defensestrategy.stratname );
                lDefenseStrategyDescription.setText("Defense Strategy Description: " + strat.defensestrategy.description );
    }
