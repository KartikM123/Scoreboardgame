    public  void updateScoreLabels(){
        lPointsH.setText(""+sc.home.scored + " | " + sc.q1[qnum].home.scored);
        lPointsA.setText(""+sc.away.scored + " | " + sc.q1[qnum].away.scored);
        
        lFGPctH.setText(""+sc.home.fgpct+ " | " + sc.q1[qnum].home.fgpct);
        lFGPctA.setText(""+sc.away.fgpct + " | " + sc.q1[qnum].away.fgpct);
        
        l3PtPctH.setText(""+sc.home.threepct + " | " + sc.q1[qnum].home.threepct);        
        l3PtPctA.setText(""+sc.away.threepct+ " | " + sc.q1[qnum].away.threepct);
        
        lReboundH.setText(""+sc.home.rebound+ " | " + sc.q1[qnum].home.rebound);      
        lReboundA.setText(""+sc.away.rebound+ " | " + sc.q1[qnum].away.rebound);
        
        lAssistH.setText(""+sc.home.assists+ " | " + sc.q1[qnum].home.assists);
        lAssistA.setText(""+sc.away.assists+ " | " + sc.q1[qnum].away.assists);
        
        lTurnoverH.setText(""+sc.home.turnover+ " | " + sc.q1[qnum].home.turnover);
        lTurnoverA.setText(""+sc.away.turnover+ " | " + sc.q1[qnum].away.turnover);
        
        lBlockH.setText(""+sc.home.block+ " | " + sc.q1[qnum].home.block);
        lBlockA.setText(""+sc.away.block+ " | " + sc.q1[qnum].away.block);
        
        lStealH.setText(""+sc.home.steal+ " | " + sc.q1[qnum].home.steal);
        lStealA.setText(""+sc.away.steal+ " | " + sc.q1[qnum].away.steal);
        
        lQuarter.setText("Quarter: " + (qnum+1));
    }
